package basic_programs;

public class RelationalOperator {

	public static void main(String[] args) {
//Declaration and Initialization
int num1=20;
int num2=10;
//Display
System.out.println("LE "+(num1<=num2));
System.out.println("GE "+(num1>=num2));
System.out.println("NE "+(num1!=num2));
System.out.println("LT "+(num1<num2));
System.out.println("GT "+(num1>num2));
	}
}