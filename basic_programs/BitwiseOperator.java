package basic_programs;

public class BitwiseOperator {

	public static void main(String[] args) {
int num1=2;
int num2=4;
System.out.println("Bitwise AND is "+(num1&num2));
System.out.println("Bitwise OR is "+(num1|num2));
System.out.println("Bitwise XOR is "+(num1^num2));/* To check on the Internet */
System.out.println("Bitwise Rightshift is "+(num1>>num1));
System.out.println("Bitwise Leftshift is "+(num1<<num2));
	}

}
